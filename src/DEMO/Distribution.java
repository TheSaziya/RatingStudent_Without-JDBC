package DEMO;

public class Distribution 
{
	public String AssCat;
	public int weight;
	

	

	public Distribution(String AssCat, int weight) {
		this.AssCat=AssCat;
		this.weight=weight;
	}

	


	public String getAssCat() {
		return AssCat;
	}

	public void setAssCat(String assCat) {
		AssCat = assCat;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}	
	
	
	
}


